package com.storyEngine.window;

public class WindowStyle 
{
	public String title;
	public int width;
	public int height;
	public int closeOperation; 
	public WindowStyle(String title, int width, int height, int close)
	{
		this.title = title;
		this.width = width;
		this.height = height;
		this.width = width;
		this.closeOperation = close;
	}
}
